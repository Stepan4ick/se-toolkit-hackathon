let currentLang = localStorage.getItem('lang') || 'ru';

function t(key) {
    return translations[currentLang][key] || key;
}

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('lang', lang);
    document.documentElement.lang = lang;
}

function getLanguage() {
    return currentLang;
}
